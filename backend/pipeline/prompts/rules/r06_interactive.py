    "    No aplicar .reveal ni IntersectionObserver a elementos dentro de carousel-slide.\n"
    "    Razon: los slides fuera de la vista tienen opacity:0 y el observer no los detecta\n"
    "    porque estan clippeados (no intersectan el viewport). Quedan invisibles.\n"
    "    SOLUCION: el carrusel no usa .reveal. Las imagenes estan visibles desde el start.\n\n"

