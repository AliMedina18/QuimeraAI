    "  function applyFilters() {\n"
    "    var searchTerm = searchInput ? searchInput.value.trim().toLowerCase() : '';\n"
    "    galleryItems.forEach(function(item) {\n"
    "      var cat = (item.dataset.category || '').trim();\n"
